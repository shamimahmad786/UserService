package com.me.user.UserService.bean;

public class DropdownBean {
private Integer userId;
private String userName;
private String dependentFlag;
private String regionCode;
private String dependentValue;



public Integer getUserId() {
	return userId;
}

public void setUserId(Integer userId) {
	this.userId = userId;
}

public String getUserName() {
	return userName;
}

public void setUserName(String userName) {
	this.userName = userName;
}

public String getDependentFlag() {
	return dependentFlag;
}

public void setDependentFlag(String dependentFlag) {
	this.dependentFlag = dependentFlag;
}

public String getRegionCode() {
	return regionCode;
}

public void setRegionCode(String regionCode) {
	this.regionCode = regionCode;
}

public String getDependentValue() {
	return dependentValue;
}

public void setDependentValue(String dependentValue) {
	this.dependentValue = dependentValue;
}





}
